#include "../inc/checker.h"

void	show_tab(int *tab, int len)
{
	int i;

	i = 0;
	ft_putchar('\n');
	while (i < len)
		ft_putendl(ft_itoa(tab[i++]));
	ft_putchar('\n');
}
